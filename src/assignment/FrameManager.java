    /**
     * Button to add new builder to the database.
     *
     * @author mjwatters
     * @Date 01/05/2018
     *
     * @param evt
     */
    private void btnAddBuilderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddBuilderActionPerformed

        boolean done = databaseConnection.insertBuilder(Integer.parseInt(txtBuilderManagerID.getText()),
                txtBuilderFirstName.getText(),
                txtBuilderSecondName.getText(),
                txtBuilderPassword.getText(),
                txtBuilderJobRole.getText(),
                txtBuilderLoginName.getText()
        );
        if (done == true) {
            JOptionPane.showMessageDialog(this, "Builder added", "Adding Record", JOptionPane.PLAIN_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(this, "Order not added", "Error", JOptionPane.PLAIN_MESSAGE);
        }//eise
    }//GEN-LAST:event_btnAddBuilderActionPerformed

